//5-25      Multiplos de 5
public class Multiplosde5 {
    public static void main (String  args){
        int i ;
        for (int i = 5; i<= 25; i++) {
            System.out.println("i:"+ i);
        }
        do{
            int i = 5;
            System.out.println("i:"+i);
            i++;
        } while (i<=5);
    }
}
