public class OtraTabladeMultiplicar{
    private static final int M=10;


public static void name(String[] args) {
   for(int i = 1; i<=M; i++) {
       System.out.println("Tabla del "+ i);
       for(int j=1; j<=M; j++){
           System.out.println(i+"x"+j+"="+i*j);
       }

   }
}
} 